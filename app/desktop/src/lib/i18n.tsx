const initialState: LanguageProviderState = {
  language: "en",
  setLanguage: () => null,
  toggleLanguage: () => null,
  t: (_key, fallback = "") => fallback,
};

const LanguageProviderContext = createContext<LanguageProviderState>(initialState);

function interpolate(value: string, params?: Record<string, string | number>) {
  if (!params) return value;
  return Object.entries(params).reduce(
    (result, [key, replacement]) => result.split(`{${key}}`).join(String(replacement)),
    value,
  );
}

export function LanguageProvider({
  children,
  defaultLanguage = "en",
  storageKey = "listency-language",
}: LanguageProviderProps) {
  const [language, setLanguageState] = useState<Language>(() => {
    const storedLanguage = localStorage.getItem(storageKey);
    return storedLanguage === "en" || storedLanguage === "zh" ? storedLanguage : defaultLanguage;
  });

  const value = useMemo(
    () => ({
      language,
      setLanguage: (nextLanguage: Language) => {
        localStorage.setItem(storageKey, nextLanguage);
        setLanguageState(nextLanguage);
      },
      toggleLanguage: () => {
        const nextLanguage = language === "en" ? "zh" : "en";
        localStorage.setItem(storageKey, nextLanguage);
        setLanguageState(nextLanguage);
      },
      t: (key: string, fallback = key) => translations[language][key] ?? translations.en[key] ?? fallback,
    }),
    [language, storageKey],
  );

  return <LanguageProviderContext.Provider value={value}>{children}</LanguageProviderContext.Provider>;
}

export function useI18n() {
  const context = useContext(LanguageProviderContext);
  if (!context) {
    throw new Error("useI18n must be used within a LanguageProvider");
  }
  return context;
}

export function formatMessage(template: string, params?: Record<string, string | number>) {
  return interpolate(template, params);
}

export function translateStatus(value: string | null | undefined, t: (key: string, fallback?: string) => string) {
  if (!value) return "-";
  const normalized = value.trim().toLowerCase().replace(/[_\s-]+/g, "_");
  const key = statusKeyMap[normalized];
  if (key) return t(key);
  return value
    .replace(/[_-]+/g, " ")
    .split(" ")
    .filter(Boolean)
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase())
    .join(" ");
}

